const allQuestions = [...questions, ...additionalQuestions];

let activeTopic = "הכל";
let query = "";
const progressKey = "south-prosecution-study-progress";
let progress = JSON.parse(localStorage.getItem(progressKey) || "{}");

const topics = ["הכל", ...Array.from(new Set([...cases.map(item => item.topic), ...allQuestions.map(item => item.topic)]))];

const $ = selector => document.querySelector(selector);

function sourceLink(id) {
  const source = sources.find(item => item.id === id);
  return source ? `<a href="${source.url}" target="_blank" rel="noreferrer">${source.title}</a>` : "";
}

function matches(item) {
  const text = JSON.stringify(item).toLowerCase();
  const byTopic = activeTopic === "הכל" || item.topic === activeTopic;
  const bySearch = !query || text.includes(query.toLowerCase());
  return byTopic && bySearch;
}

function renderFilters() {
  $("#topicFilters").innerHTML = topics.map(topic => `
    <button class="filter-button" type="button" aria-pressed="${topic === activeTopic}" data-topic="${topic}">${topic}</button>
  `).join("");
}

function renderCases() {
  const filtered = cases.filter(matches);
  $("#caseGrid").innerHTML = filtered.length ? filtered.map(item => {
    const done = Boolean(progress[item.id]);
    return `
      <article class="case-card">
        <div class="tags">
          <span class="tag">${item.topic}</span>
          <span class="tag ${item.verification.startsWith("גבוה") ? "verified" : "partial"}">${item.verification.split(":")[0]}</span>
        </div>
        <h3>${item.title}</h3>
        <p><strong>מספר/זיהוי:</strong> ${item.caseNo}</p>
        <p>${item.summary}</p>
        ${item.stateClaim ? `<p><strong>טענת המדינה:</strong> ${item.stateClaim}</p>` : ""}
        ${item.otherClaim ? `<p><strong>טענת הצד השני:</strong> ${item.otherClaim}</p>` : ""}
        ${item.outcome ? `<p><strong>תוצאה/לקח:</strong> ${item.outcome}</p>` : ""}
        <p><strong>איך לתפעל בראיון:</strong> ${item.useInInterview}</p>
        <p><strong>מקורות:</strong> ${item.sources.map(sourceLink).join(" | ")}</p>
        <div class="case-actions">
          <button class="mark-button ${done ? "done" : ""}" type="button" data-case="${item.id}">${done ? "נלמד" : "סמן כנלמד"}</button>
        </div>
      </article>
    `;
  }).join("") : `<div class="empty">לא נמצאו תיקים שמתאימים לחיפוש הנוכחי.</div>`;
}

function renderQuestions() {
  const filtered = allQuestions.filter(matches);
  $("#qaList").innerHTML = filtered.length ? filtered.map(item => `
    <details class="qa-item">
      <summary>${item.q}</summary>
      <div class="answer">
        <p>${item.a}</p>
      </div>
    </details>
  `).join("") : `<div class="empty">לא נמצאו שאלות שמתאימות לחיפוש הנוכחי.</div>`;
}

function renderSources() {
  $("#sourcesList").innerHTML = sources.map(source => `
    <article class="source-item">
      <a href="${source.url}" target="_blank" rel="noreferrer">${source.title}</a>
      <p>${source.note}</p>
    </article>
  `).join("");
}

function renderStudyPath() {
  $("#studyPath").innerHTML = studyPath.map(item => `<li>${item}</li>`).join("");
}

function renderStats() {
  $("#caseCount").textContent = cases.length;
  $("#questionCount").textContent = allQuestions.length;
  $("#sourceCount").textContent = sources.length;
}

function render() {
  renderFilters();
  renderCases();
  renderQuestions();
  renderSources();
  renderStudyPath();
  renderStats();
}

document.addEventListener("click", event => {
  const filter = event.target.closest("[data-topic]");
  if (filter) {
    activeTopic = filter.dataset.topic;
    render();
  }

  const marker = event.target.closest("[data-case]");
  if (marker) {
    const id = marker.dataset.case;
    progress[id] = !progress[id];
    if (!progress[id]) delete progress[id];
    localStorage.setItem(progressKey, JSON.stringify(progress));
    renderCases();
  }
});

$("#searchInput").addEventListener("input", event => {
  query = event.target.value.trim();
  renderCases();
  renderQuestions();
});

$("#resetProgress").addEventListener("click", () => {
  progress = {};
  localStorage.removeItem(progressKey);
  renderCases();
});

if ("serviceWorker" in navigator && location.protocol !== "file:") {
  navigator.serviceWorker.register("sw.js").catch(() => {});
}

render();
